                the world's greatest assassin but was killed by Lupin.
              </p>
              <p>
                Through long and rigorous training, Goemon has perfected the art of <em>iai</em>, the katana fast-draw.
                The combination of Goemon's reflexes and <em>Zantetsuken</em>'s sharpness make for a lethal package.
                While he feels that most of the people or things that feel the touch of <em>Zantetsuken</em> are
                worthless, he does not hesitate to cut down anything or anyone that blocks his path.
              </p>
