      <p>
        Kazuhiko Kato was born in Kiritappu Village, Hamanaka, Hokkaido, Japan. His father would show him and his
        siblings cartoons on a projector and would scold him for spending money on candy. However, he was never scolded
        for buying manga. Ignoring his junior high school teacher's advice to become a teacher in the village, he would
        regularly submit cartoons to various newspapers and once one 500¥ from the Hokkaido Shimbun. In the spring of
        1953, he took a job at the Hamanaka branch of the Kushiro Red Cross Hospital and met the director Michishita
        Shunichi. Without this man's encouragment and recognition that he had a talent for drawing manga, Kazuhiko Kato
        may have never become &quot;Monkey Punch&quot;.
      </p>
      <p>
        Kazuhiko started his publishing career in 1965 with Playboy School (プレーボーイ入門) under the pen name Gamuta
        Eiji (がむた永二) and was currently writing Pinky Punky (ピンキィ パンキィ), when he was approached to do a three
        month project for a new magazine called
        <a href="http://en.wikipedia.org/wiki/Weekly_Manga_Action" target="_blank" class="linked color-green">Weekly Manga Action</a>.
        The editor suggested Kazuhiko use the pen name &quot;Monkey Punch&quot;; Kazuhiko reluctantly agreed to use it
        for a year. The three month project and one year pseudonym turned into a franchise that has spanned decades and
        a name that is still recognized in the anime and manga community.
      </p>
      <p>
        <em>(Summarized from a 2001 April 24 article in the Hokkaido Shimbun)</em>
