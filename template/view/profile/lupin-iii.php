            <p>
              Lupin's age and nationality are unknown; there is speculation he is half-French, half-Japanese. He is
              179 cm. tall (5' 10.5&quot;) and weighs 63 kg. (~138 lbs.). He is seldom seen without his trusty Walther P-38
              and a pack of <a href="http://en.wikipedia.org/wiki/Gitanes" target="_blank" class="linked color-green">Gitanes Caporal</a>
              cigarettes.
            </p>
            <p>
              Due to the various copyright concerns about the Lupin name, Lupin III has been forced to use some unique
              pseudonyms when broadcast in various countries. In English speaking countries, he has gone by
              &quot;The Wolf&quot;, &quot;Cliff Hanger&quot; (used officially for some time on English language
              promotion by TMS), &quot;Chase Tracer&quot;, and &quot;Rupan&quot; (a literal reading of the katakana for
              his name). France gave him the more fanciful name of &quot;Edgar de la Cambriole&quot;. Germany takes the
              prize for &quot;Hardyman&quot;.
            </p>
