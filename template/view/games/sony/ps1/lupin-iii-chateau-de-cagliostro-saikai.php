      <p>While this game does have puzzles for you to solve, its main purpose seems to be to act as an interactive encyclopedia for the film. You are a tourist wandering around Cagliostro seeing the sites and gathering information about Lupin's exploits in Cagliostro. A map contains several boxes that can be stamped by solving various puzzles or answering questions correctly. Once all the boxes have been stamped, you may enter the castle itself.</p>
      <p>Along the way you can also find gold coins; these coins can be exchanged at the souvenir shop for a photo album and photos of various scenes from the film. As you interact with the people and places of Cagliostro, you come across various items you can examine. These items provide video clips, sound clips, pictures, and background text on the people, places, weapons, and items used in the film. Knowledge of the Japanese language (spoken and written) is helpful but not necessary. If you get stuck, you can consult the <a href="http://www.gamefaqs.com/console/psx/game/573171.html" class=""linked color-green" target="_blank">FAQ</a>.</p>
