      <p>Lupin has decided to go after several of his old enemies treasure; the only problem is that the treasure is hidden and is being guarded by not only his enemy's troops but also by Zenigata himself! The treasure will only be revealed when you find the key that is placed somewhere on each level. To avoid Zenigata and the various enemies, you are armed with the ability to hop on the blocks of the level. Sounds like a useless ability doesn't it? Fortunately, this causes the block you are standing on to rise up carrying you out of reach of your enemy's grasp. It also allows you to jump across the pits that are scattered around the level.</p>
      <p>Of course, pits are not the only obstacles you must overcome. A time limit, slippery ice, floors that throw you in unexpected directions, and even Zenigata throwing handcuffs at you prevent you from stealing the treasure. As always, Lupin comes equipped with a variety of gadgets to assist him. Occasionally, you will find a walkie-talkie on the level. Jumping onto it will call Jigen or Goemon to help you. While you go after the treasure, Jigen or Goemon will run around taking out any enemies in their path.</p>
      <p>This is an entertaining yet difficult puzzle game; each enemy has several levels you must complete. As the levels progress, the obstacles and enemies increase making it very difficult to steal the treasure before time runs out. The music is standard fare but also helps you establish a rhythm as you try to complete each level. Unfortunately, the game has no mechanism for continuing the game at a later time; once you turn the game off and back on, you have to start over from the beginning. Otherwise, this is a highly addictive game and well worth playing over and over.</p>
