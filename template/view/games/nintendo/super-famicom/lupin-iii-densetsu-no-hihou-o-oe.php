      <p>A quiet night at the hideout is interrupted by a large man breaking down the door; a lady follows the man in and informs Lupin that Fujiko has been kidnapped. If Lupin wants to see her again, he has to find the fountain of youth. Lupin must battle through a skyscraper to rescue Fujiko and then an ancient castle to find the fountain of youth. To help him on his mission, Lupin arms himself with his trusty Walther P-38 and an array of gadgets.</p>
      <p>At first glance, this game appears to be an ordinary platform game that uses the Lupin characters for atmosphere. Once you begin playing though, you find that the levels are intricate mazes. To proceed to the next level, you must use your gadgets and wits to not only find the exit but find out how to open up the exit. Blocking your way are electrified floors, security robots, spike lin ed pits, ceiling mounted guns, and the occasional level boss.</p>
      <p>The background music is adequate though it does not seem to fit the overall mood of the game. After completing each level, you are given a password using pictures of the Lupin cast. Overall, the game is not revolutionary, but the levels have enough variety to keep you entertained for hours on end.</p>
