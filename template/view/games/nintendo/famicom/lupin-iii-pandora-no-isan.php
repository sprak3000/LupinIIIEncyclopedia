      <p>It seems Clarice just cannot stay out of trouble. It was bad enough that she kidnapped and was almost forced into marrying the Count of Cagliostro; now, she has been kidnapped yet again and will not be released until &quot;Pandora's Inheritance&quot; is handed over to her kidnappers. Lupin, Jigen, and Goemon must once again rush to her rescue.</p>
      <p>At the beginning of the game, you may choose to play as either of our three heroes. Each one has their specialty. Lupin is armed with his trusty Walther P-38 and can use a variety of gadgets. Jigen is armed with his favorite Colt that can fire farther than Lupin's Walther. Goemon is armed with his trusty Zantetsuken and is the fastest character. Jigen and Goemon can use a limited set of the available gadgets.</p>
      <p>Gadgets are collected by breaking open the white briefcases you find along the way. One of the gadgets is a walkie-talkie that allows you to switch between the three characters. If your character is caught by any of the enemies or traps along the way, you must now choose to play as either of the two remaining characters. Fear not though; along the way, you can rescue your fallen comrade and use him along the way again.</p>
      <p>The music is pretty standard music for a Famicom game. While the graphics seem dated and primitive, the gameplay is quite difficult. Each level boss has a particular weakness that one of the characters can exploit. The enemies are numerous, and the levels are fairly large mazes requiring you to find the door that hides the level boss. Overall, it is a fairly addictive game.</p>
