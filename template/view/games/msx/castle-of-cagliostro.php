      <div><strong>Submitted by P. Lesparre</strong></div>
      <p>The game has 8 stages. To finish a stage you have to find the two rings and the exit. There are a number of items to be found, like bullets and flashlights. In the top of the screen is a bar called "Cup"; when you pick up green (small) or red (big) cups of noodles it'll grow. When you find a hot water kettle, your life will be partially renewed depending on the amount of &quot;Cup&quot; acquired.</p>
      <p><strong>Stage 1:</strong> First of all, hit all goatheads; some contain hidden items. One of the hidden items is a rocket with a rope attached so you'll be able to go up in the upper room. Stand under the recess in the wall and press <strong>Up and A-button</strong>. The rocket is now launched and you are a level higher. Stand under the big goathead and jump using the <strong>B-button</strong> or <strong>GRAPH-key</strong>.</p>
      <p><strong>Stage 2:</strong> Hit all the busts to open the trap-door in the rightmost room. Of course, you'll have to hit all the goatheads too. There are also hidden items behind the lights. In the cellar, hit the chests on the left for extra items. At the big goathead, go up; you found another rocket if you have done everything right. Take everything, go back to the goathead, and jump under it. In principle, all the other stages are finished similarly.</p>

