        <h2>E-mail Interview with Robert Woodhead</h2>
      </div>
      <p>Interview with Robert Woodhead, CEO, AnimEigo by Luis A. Cruz on 2003 May 19</p>
      <dl class="dl-horizontal">
        <dt>Q:</dt><dd>What prompted you to pursue licensing some Lupin titles?</dd>
        <dt>A:</dt><dd>They fit our general "good stuff only" requirement.</dd>
        <dt>Q:</dt><dd>Why did you decide to pick up Fuma and Babylon? Prior relationship with the licensor? Cheapest titles to pick up at the time? Quality of source materials?</dd>
        <dt>A:</dt><dd>They were the best available titles from that licensor at that time.</dd>
        <dt>Q:</dt><dd>How well have Fuma and Babylon sold over time? Did the sales of the Fuma dub on VHS & LD influence the decision to not dub "Babylon" when it was first released?</dd>
        <dt>A:</dt><dd>They have done OK but not exceptionally. We made a decision it was worth dubbing one of them, and Fuma was the more commercial title, so it got the dub. If they had sold better, we would have dubbed Babylon as well.</dd>
        <dt>Q:</dt><dd>Have you noticed a growth in sales or demand as Lupin has become more popular in the US?</dd>
        <dt>A:</dt><dd>Perhaps a little; they basically sell steadily.</dd>
        <dt>Q:</dt><dd>With Funimation and Pioneer selling under the "Lupin" name, do you feel the licensing restriction to use "Rupan" rather than "Lupin" is hurting sales of the Fuma DVD?</dd>
        <dt>A:</dt><dd>That is very difficult to say with any certainty. In any case, water under the bridge.</dd>
        <dt>Q:</dt><dd>Are there plans to release Babylon on DVD in the near future? If so, are you looking into dubbing it before releasing it on DVD?</dd>
        <dt>A:</dt><dd>If the Fuma DVD does OK, you never know.</dd>
      </dl>

