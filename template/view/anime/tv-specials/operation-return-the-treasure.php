                  <h4>Synopsis</h4>
                </div>
                <p>
                  &quot;The Trick Diamond&quot; is the key to unlocking a treasure hidden within Gaud&iacute;'s famous
                  Park Gruell. The crime lord Mark Williams was in possession of the diamond but has recently passed
                  away. However, his will gives Lupin a chance to gain the object of many a criminal's desire.
                </p>
                <p>
                  Lupin is in for quite a shock though as Mark's conditions are quite unique. Mark replaced six
                  treasures with copies and has kept the originals for himself. Now, Lupin must prove his talent by
                  switching the originals back into place within seven days. Only then will he be given the location of
                  the diamond.
                </p>
                <p>
                  With Zenigata and a gang controlled by a man named &quot;Rats&quot; hot on his trail, can Lupin
                  return the treasure in time and unlock Gaud&iacute;'s secret?!
