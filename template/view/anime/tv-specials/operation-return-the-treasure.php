          <div id="review" class="tab-pane fade in">
            <p>
              After the 2002 special <em><a class="linked color-green" href="/anime/tv-specials/episode-0-first-contact">Episode:0</a></em> became
              the new standard of excellence, one had to wonder if the 2003 special would be able to reach the same
              level of quality. <em>Operation: Return the Treasure!!</em> provides an enjoyable, action-filled romp that
              comes close to that level but suffers from an uninspired middle act.
            </p>
            <p>
              After thirty years of capers, the writers for this special have managed to come up with a fresh and unique
              angle. For once, Lupin and the gang must put things back rather than take them away. This is the core of
              the special and is a delightful break from the familiar Lupin formula. To obtain the key to a great
              treasure, Lupin must replace six phony treasures with the originals within seven days. The action begins
              immediately and does not let up until the last treasure is in place.
            </p>
            <p>
              Unfortunately, the replacement of the last treasures reveals the major flaw of this special. The middle
              act sets up the climax of the film but slows the film's pace down considerably. Until this point, the
              action and narrative were weaved together fluidly and kept the film moving nicely. Once you hit the
              middle, the story reverts to simply moving the characters around until they are ready for the final
              confrontation. While the narrative was necessary, it did feel like the writers were attempting to stretch
              the story to fit the ninety-minute time slot.
            </p>
            <p>
              One can overlook this flaw though as the special provides a healthy dose of comedy along with the action.
              There are numerous sight and physical gags throughout the special with Zenigata on the receiving end for
              most of them. The special also included some subtle references to other films such as <em>Roman Holiday</em>
              and even a subtle nod to <em><a class="linked color-green" href="/anime/theatrical-films/secret-of-mamo">Mamo</a></em>.
            </p>
            <p>
              Two other items make this special stand out. First, Goemon has a strong female adversary in Misha,
              something not seen since Kikyo in <em><a class="linked color-green" href="/anime/tv-specials/dragon-of-doom">Zantetsuken</a></em>. Though
              the setup for their rivalry is a bit contrived, it was great to see Goemon do battle against a worthy
              opponent. The second item was the lack of the usual &quot;damsel in distress&quot;. While an ing&eacute;nue
              is brought in near the end, she is neither placed in danger or is the focal point of the plot. This was a
              refreshing change of pace and combined with Fujiko's diminished role allowed the focus to rest squarely on
              the deliciously evil Misha.
            </p>
            <p>
              Despite the slow middle, this was an entertaining and amusing special. It is clear the writers are trying
              to capitalize on the new life <em><a class="linked color-green" href="/anime/tv-specials/episode-0-first-contact">Episode:0</a></em>
              has given the franchise. <em>Operation: Return the Treasure!!</em> provided some refreshing new angles on
              the Lupin formula and proved that Lupin can outrun not only Zenigata but also the specter of becoming a
              dated series.
            </p>
