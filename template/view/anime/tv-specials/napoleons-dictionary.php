          <div id="review" class="tab-pane fade in">
            <p>
              &quot;Are we there yet?&quot; This is the phrase viewers of Napoleon's Dictionary will utter through most of
              the special. Lupin must participate in an antique car grand prix across Europe to reclaim the dictionary and
              the key to a lost Lupin family treasure contained within. However, the ride only manages to be mildly
              entertaining relying on gadgets and a few comedic moments to pass the time.
            </p>
            <p>
              The special does manage to get a few pieces right; there are comedic touches that will have you laughing aloud.
              Save for Fujiko, all of the characters are heavily involved in the plot, and the plot itself is well paced and
              animated. Goemon fans will also enjoy his sword work at the end of the special.
            </p>
            <p>
              What works the most is the actual treasure for this special; Lupin knows little about it other than it was
              spirited away from his grandfather by a neglected wife. When the treasure is revealed, the result is a familiar
              one but is given a unique twist not seen in other Lupin titles. One has to applaud the writers for producing a
              fresh ending, but the sum of this special's parts just does not add up to a title worth watching more than once.
            </p>
          </div>

