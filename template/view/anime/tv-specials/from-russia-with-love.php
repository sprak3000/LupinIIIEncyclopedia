          <div id="review" class="tab-pane fade in">
            <p>
              <em>From Russian with Love</em> had an interesting premise; Lupin is after the lost gold of the Romanov empire
              but must face the New York mafia and the psychotic mystic Rasputin. The execution of the premise was
              unfortunately very bland making this another mediocre entry. This was not due to pacing problems; the film
              flowed well and kept the characters moving. It simply lacked the blend of interesting villains, action, and
              comedy that makes for a good Lupin title.
            </p>
            <p>
              The bulk of the special is spent simply finding and retrieving the gold; the mafia becomes involved when Lupin
              finally steals the gold. Rasputin spends the bulk of the special inserting his finger into Goemon's facial
              orifices. Even Zenigata's involvement is relegated to the periphery; he chases Lupin at the beginning of the
              special and has no influence on the plot in his brief appearance later. Lupin has no immediate rivals for the
              treasure making for a dull heist that involves a lot of shoveling.
            </p>
            <p>
              Eventually, Rasputin simply snatches the gold away from Lupin via a large plane and takes to Siberia. One would
              expect a climatic battle through Rasputin's castle between Lupin, the mafia, and Rasputin. However, Rasputin
              eliminates the mafia shortly after they appear, and Lupin uses the same plane to take the gold back. The final
              showdown between Rasputin and Lupin is brief and anti-climatic. With no heat built up between the two, it was
              merely a device used to bring the story to its end.
            </p>
            <p>
              The animation style also worked against this special; while it was on par with other titles from the early
              1990s, it overused some elements, most notably stopping the action and turning it into a pastel image. It made
              the special feel it was produced on a very tight budget forcing the animators to cut corners.
            </p>
            <p>
              This special did not do anything bad, but it certainly did not do much well either. It focused mostly on the
              heist but did little to make the heist interesting. The result is not the worst Lupin title produced, but it
              does not have the content to merit repeated viewings.
            </p>
          </div>

