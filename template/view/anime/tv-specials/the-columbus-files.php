                  <h4>Synopsis</h4>
                </div>
                <p>
                  Fujiko and Lupin are enjoying a romantic dinner together; as usual, Lupin's appetites include getting a taste
                  of Fujiko's latest plan. Fujiko has uncovered the "Columbus Files" that tell of an incredible treasure known
                  appropriately as the &quot;Columbus Egg&quot;. Before Lupin can learn more, they are attacked by a helicopter
                  squadron led by Nazaroff. Lupin and Fujiko end up hanging onto the edge of a cliff, but Fujiko falls to earth
                  after a gunshot severs the rope attaching her to Lupin.
                </p>
                <p>
                  A young woman named Rosaria is handing over a piece of sunken treasure to a buyer. She returns home to find an
                  uninvited guest... Fujiko! However, Fujiko has amnesia and is haunted by frightening images of being chased by
                  Nazaroff's men and of the contents of the Columbus Files. Can Lupin restore Fujiko's memories to find the
                  Columbus Egg as well as for her own sake? And why is Rosaria determined to find the Egg for herself?
                </p>
