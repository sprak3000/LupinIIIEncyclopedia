          <div id="review" class="tab-pane fade in">
            <p>
              <em>Da Capo of Love</em> has all the standard items we have seen in previous Lupin specials; submarines,
              betrayal, ancient treasures that hold mystical power, mysterious relations... there is little material in the
              special that has not been seen previously in some form. What is different about this special is its treatment
              of one of the main characters. Thanks to the convenient plot device of amnesia, Fujiko spends most of the
              special defenseless and actually trying to help Lupin rather than cheat him.
            </p>
            <p>
              However, it is how Lupin responds to this turn of events that is the interesting aspect of this contrived
              situation. With no memory of who she is or what she is capable of, Lupin must play the role of protector rather
              than lecher; it allows him to show the side of him that truly does love Fujiko rather than the side that lusts
              after her. This aspect is not overplayed to the point of being maudlin though; it appears in just the right
              moments and does not linger long.
            </p>
            <p>
              The special did manage to excel in its humor as well; there were a number of amusing jokes and sight gags.
              Balancing action and humor is a hallmark of the better Lupin specials, and this one came close to achieving
              that balance. There were plenty of scenes that had me laughing out loud, but the action was not up to par. <em>Da
                Capo of Love</em> does manage to be slightly better than the average Lupin special, but it will not likely top the
              list of most Lupin fans.
            </p>
          </div>

