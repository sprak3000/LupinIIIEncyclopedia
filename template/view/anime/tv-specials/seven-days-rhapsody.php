          <div id="review" class="tab-pane fade in">
            <p>
              While I cannot say it was terrible, I cannot also say that it was particularly good either. It opens at
              the Aquaduct Race Track where Zenigata is keeping a watchful eye out for Lupin. He is drawn to the
              familiar sound of the engine of Lupin's Fiat 500. However, he only spies Jigen in the car and realizes
              that Lupin must still be on the grounds.
            </p>
            <p>
              Lupin has disguised himself as a jockey and has fixed the race in order to win the large wagers he has put
              down with local bookies. Zenigata sees through the disguise and promptly tries to arrest him in the locker
              room. In an amusing moment, Zenigata has shown that he has learned from past encounters. Rather than
              simply pulling Lupin's mask off, he orders in a bomb squad and dumps the mask into steel box where it
              explodes harmlessly. While a small detail, it was refreshing and amusing to see the franchise
              acknowledging that a particular gag has been used so often that even Zenigata has caught on to it.
            </p>
            <p>
              From there, Lupin runs into a young girl named Michelle; Michelle is the daughter of a very rich man and
              is being chased by some thugs. Lupin bails her out of trouble briefly, but the thugs eventually manage to
              take her. Jigen has hooked up with an old mercenary friend he last worked with in South Africa. We get a
              brief glimpse into Jigen's past in South Africa, but this does not come into play beyond connecting him to
              the mercenary. This leaves Goemon working for Fujiko in order to steal a large diamond.
            </p>
            <p>
              The three plot lines converge into a rather banal story where Michelle's father and Jigen's friend are
              trying to double-cross everyone to obtain the diamond and the details of the NDW system that turns
              diamonds into powerful explosives. It is difficult to call the special completely boring or terrible; it
              was paced well and kept the narrative and action decently balanced. However, the various plots just did
              not tie together to make for a compelling story.
            </p>
            <p>
              This special is a prime example of what I believe is needed to reinvigorate the franchise. A new Lupin TV
              series should be produced, something that would like <em>Ghost in the Shell: Stand Alone Complex</em>. The
              premise of the special, indicated in the title, was that the events were to take place over seven days.
              However, only having ninety minutes in which to work, the various plot threads could do little but chase
              MacGuffins around with few consequences.
            </p>
            <p>
              Jigen was forced into hunting down Lupin; while this produced some decent action sequences, their conflict
              was brief and had little effect on the plot. This can be said of all three plot lines; the NDW system is
              briefly mentioned and used making it feel tacked on to keep the plot moving and to tie it into Fujiko's
              plot. All these elements and plot devices converge but were given so little development that the ending is
              anti-climatic.
            </p>
            <p>
              But given enough time, these elements could have developed into richer story. Looking at this special and
              the previous two, this seems to be a common theme. The premise of the special is interesting, but there
              just is not enough room in ninety minutes to develop more than paper-thin supporting cast, villains, and
              plots. The time has come for TMS to invest in a new TV series for Lupin and give it a chance to capture a
              larger portion of the new generation.
            </p>
