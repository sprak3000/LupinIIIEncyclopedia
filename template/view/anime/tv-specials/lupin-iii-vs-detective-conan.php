          <div id="review" class="tab-pane fade in">
            <p>
              When this cross-over was first announced, I was cautiously optimistic on how well the two franchises would mesh. Master detective versus a master thief... this is definitely a formula that could produce some solid entertainment. After it aired, I found myself in no particular hurry to sit down and watch it. The little promotion I saw of the special did little to pique my interest. I have only a passing knowledge of the <em>Detective Conan</em> series, but this special can be accurately summed up as a Conan story that just happens to feature the cast from <em>Lupin the Third</em>.
            </p>
            <p>
              Lupin vs Conan... while not an original title, it grabs one's interest by conjuring imagery of an epic clash between two of the sharpest knives in anime's drawer of intellect. Yet, the titular pair do not even meet until an hour into the special; from there, they barely cross paths again until the end where the manage to help each other uncover the truth behind the the death of Mira's mother and brother.
            </p>
            <p>
              Setting aside the lack of understanding of &quot;versus&quot; by the writer, the story does work well as a mystery and serves as a good introduction to the Conan franchise. You come away with a sense of how the series blends humor into its narrative without slighting Conan's deductive reasoning. As a fan of Lupin, you come away with some decent visual nods to the franchise but otherwise unimpressed by the lack of action.
            </p>
          </div>

