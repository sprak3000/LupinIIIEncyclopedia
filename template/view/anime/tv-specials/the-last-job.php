          <div id="review" class="tab-pane fade in">
            <p>
              It is nearly a year since <em>The Last Job</em> aired on Japanese TV; since that time, I have had the original TV broadcast, a fansub of said broadcast, and finally the actual DVD sitting on my desk gathering dust. The <em><a class="linked color-green" href="/anime/tv-specials/lupin-iii-vs-detective-conan">Detective Conan</a></em> cross-over the previous year was a huge disappointment, and both the title for the 2010 special and the revelation that Zenigata was going to &quot;die&quot; left me dreading sitting down to watch another lackluster special. I finally worked up the desire to pop the DVD in, sat back, and let the ninety minutes pass by.
            </p>
            <p>
              Remarkably, the special was not as big a disappointment as I feared. The plot cribs from <em><a class="linked color-green" href="/anime/original-video-animation/plot-of-the-fuma-clan">The Fuma Clan</a></em>; a Japanese treasure is the key, this time only one piece of the key, to an even greater treasure. Two ninja clans are battling for the treasure &ndash; one to steal its secrets led by the masked man Morgana, and the other to protect its secrets. To add to the fun, the writers toss in a connection to da Vinci, a female ninja and her trusty dog, a deadly yo-yo toting rival from Jigen's past, and a ninja dressed like a bird. Lately, the specials tend to bog down in the middle with exposition on what the treasure is or might lead to. However, this special weaves the explanations in between the action nicely, making the ninety minutes fly by rather than drag on.
            </p>
            <p>
              I still do not understand why the writers continue to pull the gimmick of having a major character &quot;die&quot;. Zenigata's death barely factored into the plot, and I would wager no one in the audience actually believed he was going to stay dead. If you want to take a character off the board for most of the special, just whack them over the head and leave them in a coma. At least, that is going to be more believable. Overall, the action was decent, and there were a few genuine laughs to be had; it may not have been a fantastic special, but it helped wash out a bit of the bad taste of the 2009 entry.
            </p>
          </div>

