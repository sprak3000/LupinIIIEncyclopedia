          <div id="review" class="tab-pane fade in">
            <p>
              On the surface, <em>Angel's Tactics</em> combines a number of elements from previous specials into a
              decent but predictable story. With over thirty years of heists and capers, Lupin has pretty much left his
              mark on nearly every recognizable facility around the world. Area 51 provided a great opening sequence
              that showcased the combination of humor and action that has kept Lupin going over the years.
            </p>
            <p>
              Unfortunately, the story seems to lose its way once Lupin has made off with the alien Original Metal. The
              remainder of the special features the Bloody Angels attempting to grab the Metal for themselves but
              finding Lupin and his gang thwarting their efforts at every turn. What is missing from the plot is a sense
              of a larger goal or plan that the Bloody Angels have for the Metal.
            </p>
            <p>
              The best Lupin material makes the item stolen at the beginning a central piece to a larger mystery. The
              story then begins to reveal why it was a key piece to the villain's plan and why Lupin must keep it out of
              their hands. This special fails to build up any sense of mystery or grand plot leaving the viewer to watch
              the characters go through motions seen far too often.
            </p>
            <p>
              The Bloody Angels also started off as interesting characters by using their feminine charms and playing to
              each character's individual weakness towards women. However, they are soon reduced to the standard gun
              versus gun, sword versus sword archetypes with Lupin mopping up the rest with his Walther.
            </p>
            <p>
              One bright spot in the special was Lupin's interaction with Sophia, one of the Bloody Angels. She
              struggles to take down Lupin at every turn, but Lupin sees through her every effort. Their conversations
              and the way they play out in the story was one of the more enjoyable aspects and brought a bit more than
              the standard action formula seen in a number of the specials.
            </p>
            <p>
              Overall, it is difficult to label this as a mediocre special; it kept a nice pace going and never felt
              boring. It had plenty of decent action sequences and comedic touches combined with homages to a number of
              past specials. Lupin and Sophia's relationship throughout also proved to be a delightful surprise, yet
              nearly everything in this special is something the audience has seen many times over. It just did not
              manage to bring a fresh angle to the plot elements to elevate it to the top tier.
            </p>
          </div>

