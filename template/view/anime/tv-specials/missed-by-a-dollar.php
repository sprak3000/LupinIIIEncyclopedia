                  <h4>Synopsis</h4>
                </div>
                <p>
                  Lupin has disguised himself and is bidding on a ring up for auction in New York City. The ring holds
                  the secret location of a legendary broach; the broach is said to bring good fortune to whoever wears
                  it and has been in the possession of Napoleon, Lenin, and Hitler. When a bidder outbids him by one
                  dollar, he decides to take the more traditional route and steal it. However, a heavily armed group of
                  men relieve him of his prize. They work for Cynthia, the head of Bank of World.
                </p>
                <p>
                  Lupin gathers the troops to try to steal the ring back from the heavily protected headquarters. He
                  succeeds in stealing it once again but appears to be fatally shot as they make their getaway. If
                  Lupin is dead, who will stop Cynthia from obtaining the broach!?
                </p>
