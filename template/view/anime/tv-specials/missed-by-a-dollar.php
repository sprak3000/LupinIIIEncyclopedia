          <div id="review" class="tab-pane fade in">
            <p>
              One of the more unique specials, <em>1$ Money War</em> manages to bring a fresh approach to the Lupin
              franchise but does not quite manage to propel itself into the upper echelon of titles. This story revolves
              around a ring inscribed with the location of a mystical broach; any dictator that has possessed the broach
              has had good fortune in all they do. Lupin's attempt to purchase the ring at auction is foiled when a
              call-in bidder snatches it away with bid that is one dollar higher than his. So begins Lupin's dealings
              with Cynthia, the ruthless head of the Bank of World.
            </p>
            <p>
              Cynthia is a worthy foe for Lupin; she is a strong, cerebral female character who is equally as devious as
              Lupin. What makes this special stand out from the standard formula is this more cerebral approach both
              Lupin and Cynthia use to battle each other. It skillfully plays the plot out as the two con men attempt to
              outwit each other at every turn. Remarkably, the pacing of the story does not suffer at all but rather
              flows along and keeps the viewer glued to the screen.
            </p>
            <p>
              There are still plenty of car chases, gun fights, action, and humor, as one would expect from a Lupin
              title. Of particular note is Goemon's attempts to lead a more pure life by helping a traveling priestess
              collect alms. However, he fails at the traditional methods and soon finds himself tagging along with the
              gang to procure alms in a less than conventional manner under the guise of &quot;training&quot;. The
              fruits of his training are predictable but quite amusing.
            </p>
            <p>
              The special does use one standard gimmick with little success though; the touted &quot;twist&quot; for
              this special is that Lupin apparently dies. The audience would be hard pressed to believe that Lupin would
              actually die, so it becomes a matter of when we will see him reappear. Unfortunately, it does not take him
              very long to reappear making the twist seem mostly irrelevant to the plot. Also, it would have been great
              to see the rest of the gang carry the special for a longer period; while past Lupin titles have been
              centered on a particular character, none of them have let the gang sans Lupin carry the show. A great
              opportunity to make the twist meaningful while exploring a fresh angle on the franchise was missed here.
            </p>
            <p>
              But, the story remains engaging enough to compensate for this shortcoming. It also combines some great
              vocal tracks into the special making the music memorable. A fun title to watch, 1$ Money War just lacked
              something intangible that would place it in my list of all-time favorites, but it is an accessible title
              for both the die-hard and casual Lupin fans.
            </p>
          </div>

