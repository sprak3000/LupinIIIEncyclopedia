          <div id="review" class="tab-pane fade in">
            <p>
              The recent Lupin specials have attempted to reinvigorate the series by adding some new wrinkles to the
              standard story. <em><a class="linked color-green" href="/anime/tv-specials/episode-0-first-contact">Episode:0</a></em>
              explored the past of the gang, and <em><a class="linked color-green" href="/anime/tv-specials/operation-return-the-treasure">Return the Treasure</a></em>
              had Lupin returning rather than stealing items. The 2004 special <em>Stolen Lupin</em> attempts to add
              another wrinkle but does not manage to fully explore it.
            </p>
            <p>
              Lupin finds himself at odds with a thief of the younger generation, a female thief named Becky. After
              stealing Becky's heisted loot, Lupin finds himself captured by a mysterious crime lord named Malchovich.
              With Fujiko's life on the line, Lupin must steal the &quot;Bull's Eye&quot; jewel for Malchovich. Lupin
              does just that, but Becky returns the favor and makes off with the jewel.
            </p>
            <p>
              What follows is a series of double-crosses, fighting, and mysterious connections between the characters.
              The film is well-paced and keeps the story flowing, but there is something lacking in the presentation.
              It is not a boring special and will not have you looking at the clock, but it just does not do anything to
              really captivate the audience. Most of the material has been covered by the series numerous times, but
              nothing fresh is brought to this special to distinguish it from prior work.
            </p>
            <p>
              The concept of a younger thief to challenge Lupin and become his rival was disappointingly not developed
              much beyond the first act. Here was an opportunity to test Lupin and show that after all his adventures
              that he has not lost a step. However, Becky's involvement becomes relegated to a convenient plot device
              for the final showdown between Lupin and Malchovich. Stolen Lupin is not the worst special to come around,
              but it lacks the polish and uniqueness that is the hallmark of the great specials.
            </p>
