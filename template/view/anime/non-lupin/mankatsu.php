                <p>
                  <em>Katsudou Dai Shashin &quot;Mankatsu&quot;</em> (roughly translated <em>Big Movie Show &quot;Mankatsu&quot;</em>)
                  aired on the Japanese satellite station WOWOW. The series presented a variety of short works from Monkey
                  Punch. The core of each episode were two stories; a &quot;Mini Stage&quot; story lasted fifteen minutes
                  with a thirty minute &quot;Main Stage&quot; after it. Wrapped around both of these segments were a
                  series of eight vignettes (&quot;Short Corners&quot;). Additionally, the marketing for the original
                  broadcast also mentioned a &quot;Real Copy Gravure Idol Corner&quot; segment featuring a lovely idol
                  or model. These segments were not included on the DVD release, and this author has yet to see a raw
                  broadcast recording to confirm these segments did air.
                </p>
                <p>
                  The stories and vignettes are all based on previous manga work by Monkey Punch. One of the more notable
                  vignettes is &quot;Lupin World&quot; featuring our favorite band of thieves frustrating Zenigata at
                  every turn. The name &quot;Mankatsu&quot; is given to vignettes revolving around the antics of various
                  apes and monkeys that can only speak by saying &quot;Mankatsu&quot;. Quite often, the Lupin cast would
                  show up in these shorts as well.
                </p>
                <p>
                  Nearly all of the stories showcased Monkey Punch's ribald sense of humor. Two shorts focused solely on
                  puns and wordplay intended to give the viewer one mental visual (usually sexual in nature) but then
                  reveal a completely different, innocuous visual.
                </p>
