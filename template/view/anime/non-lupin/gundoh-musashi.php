                  <dt>Japanese Title:</dt><dd><em>Gundoh Musashi</em></dd>
                  <dt>Initial Air Date:</dt><dd>2006 April 09</dd>
                  <dt>Final Air Date:</dt><dd>2006 October 01</dd>
                  <dt>Episode Count:</dt><dd>26</dd>
                  <dt>Run Time per Episode:</dt><dd>~25 minutes</dd>
                </dl>
                <div class="headline">
                  <h4>Synopsis</h4>
                </div>
                <p>
                  The war over control of a unified Japan, the Sengoku Era, is over and the Toyotomi army has won. Toyotomi has allowed his rival, Tokugawa, to live but since that final battle an evil ayakashi named Yasha reveals to Tokugawa that history says that he should have won the war instead of Toyotomi. Tokugawa agrees to the evil ayakashi power Yasha offers him, and the only person who can stop Tokugawa from ruling Japan and killing Princess Kaguya, who is the link to this alternate present, is Musashi Miyamoto, a young and brash man who follows the way of &quot;Gun-do&quot;, which mixes together swordfighting with gunplay.
                </p>
