                <p>
                  <em>Gundoh Musashi</em> aired on the Japanese satellite channel BS-i. It gained infamy as having some
                  of the worst production values seen in a modern anime; a DVD of the first few episodes came out in Japan
                  with a box set for the entire series set to be released later. That set was cancelled; the only complete
                  DVD version exists as a French release.
                </p>
