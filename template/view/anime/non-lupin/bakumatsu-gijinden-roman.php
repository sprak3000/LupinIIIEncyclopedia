              <div class="col-md-10">
                <p>
                  While many a video game has been turned into an anime, this is the first time I have heard of a pachinko
                  machine having its video clips expanded into a full blown series. Monkey Punch designed the characters
                  for a pachinko machine, and someone at TV Tokyo decided it would be brilliant to greenlit an anime around it.
                  Crunchyroll picked up the rights to subtitle and stream it online. It was not a horrible show, but it
                  did not capture enough audience for a US company to pick up the home video release rights.
                </p>
                <dl class="dl-horizontal">
                  <dt>Japanese Title:</dt><dd><em>Bakumatsu Gijinden Roman</em></dd>
                  <dt>Initial Air Date:</dt><dd> 2013 January 07</dd>
                  <dt>Final Air Date:</dt><dd>2013 March 25</dd>
                  <dt>Episode Count:</dt><dd>12</dd>
                  <dt>Run Time per Episode:</dt><dd>~24 minutes</dd>
                </dl>
                <div class="headline">
                  <h4>Synopsis</h4>
                </div>
                <p>
                  Manjir&omacr; Tatsukeya works as a jack of all trades, helping troubled people in town for not much money that he ends up losing on bets. However, at night, he turns into a Japanese-style Robin Hood, the phantom thief Roman. In order to protect the smiles of the people in his town, he continues to fight against injustice with his sister Koharu and friends.
                </p>
