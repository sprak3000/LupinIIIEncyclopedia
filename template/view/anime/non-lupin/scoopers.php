          <div id="review" class="tab-pane fade in">
            <p>
              <em>Scoopers</em> is another example of why the one shot OVA is difficult to pull off well. The story starts with a less than interesting premise; a mysterious Mister X is doing nasty things like blowing up space shuttles during launch. Who better to track down this dastardly villain than a female reporter and her trusty, and slightly lecherous, android photographer. What follows is a series of lackluster action sequences culminating in an ending that leaves room for a sequel. Why anyone would be excited enough to create or watch a sequel after this entry would be the greatest mystery to solve.
            </p>
            <p>
              The story is not necessarily awful, but there just is not enough time to really do anything interesting. It plays around with some interesting concepts &ndash; androids, pulling people into a computer ala <em>Tron</em>. However, they are just thin devices used to move the action scenes along until the end credits roll. If you are a Monkey Punch fan, you will get some slight entertainment out of it, but this is an otherwise forgettable title.
            </p>
          </div>

