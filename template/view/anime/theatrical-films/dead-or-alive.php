          <div id="review" class="tab-pane fade in">
            <p>
              Regarded by fans as one of the best Lupin titles created and Monkey Punch in the director's chair, my expectations
              were high going into this film. As the end credits rolled, the film had proved that these expectations were well
              founded. <em>Dead or Alive</em> blends the best parts of the manga and anime together to make for a fantastic film.
            </p>
            <p>
              One of the more impressive aspects of the film was the pacing of the plot and the character development. Portions
              of the film focus on the relationship between the deposed Prince Panish and the Zufu female agent Oleander.
              Rather than slowing down the film's pace, the scenes integrate themselves between the action sequences to tell
              an engaging story. The action sequences are equally impressive; the island's defense system provides for some
              beautiful sequences that combine high-energy action with the comedic touches the manga series is known for.
            </p>
            <p>
              <em>Dead or Alive</em> clearly returns to Monkey Punch's concept of Lupin with the best evidence seen in
              Zenigata's portrayal. Previous outings portrayed Zenigata as an obsessive and bumbling cop. However,
              <em>Dead or Alive</em> brings back the Zenigata seen in the manga &ndash; still obsessed with capturing Lupin
              but smart, strong, confident, and determined. While Zenigata still provides a dash of humor in this outing, it
              was refreshing to see him once again as a strong, worthy adversary for Lupin.
            </p>
            <p>
              One of the plot points is a $1,000,000 bounty placed on the head of Lupin; this plot point is given little
              airtime making the title Dead or Alive seem out of place. However, repeated viewings have made it clear that
              the title is just as sneaky as Lupin and is better suited to a larger plot point in the film. This is another
              small but skilled touch that makes the film so great.
            </p>
            <p>
              The film is worthy of the praise heaped upon it by fans; the only thing that could have made this film better
              would be if Yamada Yasuo had lived to voice Lupin. Kurita Kanichi did a great job, but you can tell he was still
              struggling with filling the large shoes Yamada left. If you have yet to see this film, do yourself a favor and
              find a copy of it as soon as you can. You are missing one of Lupin's greatest capers ever!
            </p>
          </div>

