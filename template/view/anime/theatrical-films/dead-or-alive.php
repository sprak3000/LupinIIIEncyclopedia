                  <h4>Synopsis</h4>
                </div>
                <p>
                  The latest target for Lupin is the legendary treasure of the kingdom of Zufu. This treasure lies on the mysterious
                  &quot;Drifting Island&quot;, and Lupin drags his gang along to investigate. Upon reaching a mysterious door,
                  the island activates an impressive defense system, and Lupin manages to escape by the seat of his pants.
                </p>
                <p>
                  It is up to Lupin to find the key to unlock the door to the treasure. To do this, he must avoid the psychotic
                  leader of Zufu, bounty hunters, secret police, and the indomitable Zenigata. Can Lupin unlock the secrets of
                  the island before he ends up as a $1,000,000 trophy?!
