                <p>
                  This film is notable for introducing the first new voice in the main Lupin cast since the entire cast replacement for
                  <a href="/anime/original-video-animation/plot-of-the-fuma-clan" class="linked color-green" target="_blank">Plot of the Fuma Clan</a>.
                  Sadly, Yamada Yasuo passed away on 1995 March 19 from a brain hemorrhage. Kurita Kanichi was personally tapped by
                  Yamada to be his successor based on Kurita's nearly spot-on imitation of Yamada's style. The title of the film
                  translates more literally as <em>Die, Nostradamus!</em>, but fansubbers usually referred to it as <em>To Hell with Nostradamus!</em>
                </p>
                <p>
                  The film was released in the US on DVD by Funimation alone and in a box set with other Lupin films titled <em>The Final Haul</em>.
                </p>
