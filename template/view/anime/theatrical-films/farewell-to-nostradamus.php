                <div class="headline">
                  <h4>Synopsis</h4>
                </div>
                <p>
                  A simple diamond heist leads Lupin into the machinations of a bizarre cult. The cult is based around the
                  prophecies of Nostradamus, and they kidnap Julia, the daughter of Douglas, a wealthy American who is seeking
                  the presidency, along with Lupin's diamond. At stake is the lost book of prophecy Douglas holds in the vault
                  at the top of his skyscraper.
                </p>
                <p>
                  Can Lupin rescue Julia, find a way into the vault, and discover the connection between the cult and the
                  kidnapping? And what about his diamond?!
                </p>
