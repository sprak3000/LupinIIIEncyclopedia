          <div id="review" class="tab-pane fade in">
            <p>
              Commissioned for the 40th anniversary of the franchise, there were high hopes for <em>Green VS Red</em> to
              achieve the same heights the 30th anniversary TV special
              <em><a href="/anime/tv-special/episode-0" class="linked color-green">Episode:0</a></em>. While it did not hit
              that mark, it did prove more entertaining than most recent TV specials. The major flaw with this OVA is one
              seen in most of the recent Lupin material; the major target for Lupin's heist proves to be of little
              consequence to the plot. It is simply a device aimed to move the characters into certain places and situations.
            </p>
            <p>
              So it goes with the &quot;Ice Cube&quot;, an object that is referenced a few times before being revealed for
              a few brief moments and then forgotten. The story around the Ice Cube and Nighthawks organization simply did
              not come together into an enjoyable, cohesive whole. What carried the story though was the struggle between
              the green and red jacketed Lupins and its effect on two ordinary people.
            </p>
            <p>
              Yasuo is a short order cook in a noodle stand, and his long suffering girlfriend Yuikiko is a news reporter.
              Yasuo begins to develop an obsession with Lupin, one that leads him down a path Yukiko is unwilling to follow.
              <em>Green VS Red</em> is more the tale of Yasuo's transformation than a simple heist film.
            </p>
            <p>
              The publicity and build-up behind the OVA, even the title itself, was focused around a one-on-one showdown
              between the green and the red. However, the opening quickly reveals that the showdown features not two Lupins
              but nearly a hundred of them. This gimmick proves to be a lot of fun and provides plenty of homages to the
              series along with just the right comedic touches to lighten the mood.
            </p>
            <p>
              The comedy does go too far at times, with brief, baffling moments. Much like the Ice Cube, certain flourishes
              just did not fit well with the themes they were trying to achieve with Yasuo's story. Fans of the secondary
              cast will be disappointed, as the focus is squarely on Lupin and Yasuo leaving Jigen, Goemon, Fujiko, and
              Zenigata to make only token appearances.
            </p>
            <p>
              However, it was enjoyable to see Zenigata given a semblance of a past with the Tokyo Police Department.
              Other detectives knew who he was and were actively working with him and respected him to an extent. Jigen
              does manage to provide one memorable action scene that will have his fans buzzing. Still, their screen time
              was minimal and did not factor significantly into the plot.
            </p>
            <p>
              Overall, this was an enjoyable way to celebrate 40 years of Lupin material; while not the stellar title
              <em>Episode:0</em> was, <em>Green VS Red</em> a unique blend of action, comedy, and drama not seen in the
              past few years. This is easily a title I would recommend Lupin fans pick up.
            </p>
          </div>

