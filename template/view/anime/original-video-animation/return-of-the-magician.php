              <div class="col-md-10">
                <p>
                  <em>Return of the Magician</em> was another entry in the attempt to mark an anniversary of the franchise. This
                  was created to celebrate the 30th anniversary of the Lupin anime franchise in 1971. Depending on your point of
                  view, this film came out a year late or right on time as the first TV series ended in 1972. Regardless, the DVD
                  set for the film was a remarkable set in terms of extras.
                </p>
                <p>
                  The set contained two additional discs starting with a CD-ROM interactive puzzle game that featured Zenigata attempting to locate
                  Lupin in the various locations used in the film. If you managed to beat the game, you would receive a password
                  for zip files that contained exclusive wallpapers. Never did manage to get that password&hellip; The second disc
                  was the soundtrack for the film.
                </p>
                <p>
                  The set featured TV commercial spots, eight audio interviews with the cast and crew, an art gallery of the backgrounds used
                  in the film, a gallery of production notes, and a short text-based film outlining the history of the crystals.
                  What really set this release apart from its contemporaries, and possibly anything seen since, are the extras baked into
                  the film itself. When the film reaches its climax, you can select from three different endings. It also features
                  a &quot;multi-stream&quot; mode to turn off the voice track for a particular character. Dub your own voice for
                  your favorite character! While you could only turn off one track for the duration of the film, it certainly opened the
                  door for fans around the world to create their own home-grown dubs of the film.
                </p>
                <p>
                  Unfortunately, anyone who actually watched the film would be loathe to undertake such a venture. Despite featuring
                  one of the most memorable villains of the franchise, the film was dangerously close to being a complete bore.
                  A priced down version of the DVD set stripped of most of the extras was released a year later.
                </p>
