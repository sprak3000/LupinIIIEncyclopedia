          <div id="review" class="tab-pane fade in">
            <p>
              My preconceptions going into this series were based on <em><a href="/anime/theatrical-films/legend-of-the-gold-of-babylon" class="linked color-green">Legend of the
                  Gold of Babylon</a></em>; I was expecting an arduous journey through fifty episodes. However, this third TV
              series managed to be more entertaining, though not by much. Most of the episodes are mediocre heists lacking the
              wacky panache the second series mustered. The rest range from absurd slapstick to some of the hard edge the
              first series had. It was not an exceptional run of fifty episodes, but it managed to be just entertaining enough
              to keep me watching.
            </p>
            <p>
              With passable stories, one would hope that the animation and character designs would provide some decent eye
              candy to help. Not the case with the third series; the character designs are atrocious making the cast elongated,
              amorphous blobs. Fujiko comes off the worst looking like a hideous, old crone half the time. Near the end of the
              series, the characters edge back towards more familiar designs, but multiple animators must have been working on
              the series as they shift from the familiar back to the hideous for the last few episodes.
            </p>
            <p>
              Overall, this could have been much worse; I could not have sat through fifty episodes of material like
              <em>Babylon</em>. I managed to get some enjoyment out the series, but it is not one I will be pulling off the
              shelf much.
            </p>
