              <div class="col-md-10">
                <p>
                  Around 1982, French animation studio DiC began a collaboration with the Japanese producers of <em>Lupin the
                    Third</em> to create a new animated series. Given a large budget and exceptional staff, this series flung
                  Lupin and the gang five generations into the future. The series would also be geared to a young Western
                  audience and their morals and sensibilities.
                </p>
                <p>
                  To accomplish this, the cast had to undergo some cosmetic changes. A thief could not be the &quot;hero&quot;
                  of the series, so Lupin the Eighth became a freelance detective patterned after Columbo. Smoking and realistic
                  guns were taboo forcing Jigen the Sixth to trade in his cigarette and Magnum for a lollipop and laser gun that
                  drew no blood from its target.
                </p>
                <p>
                  Oddly enough, Zenigata the Sixth carried around a pipe, but the character design sheets show that this pipe
                  was actually a miniature communicator. Now that Lupin was no longer a thief, Zenigata's pursuit was reduced
                  to chasing him simply based on their shared family history rather than on any actual crimes committed.
                  Goemon the Eighteenth was the only character to receive a cool upgrade; the Zantetsuken was replaced by a
                  lightsab... laser sword.
                </p>
                <p>
                  Sadly, the turmoil surrounding the copyright and use of the Ars&egrave;ne Lupin name doomed this project to
                  failure. Not a single episode was completed and aired. Very little information about this project has
                  survived since it folded. One rumor has Rintaro listed as the Japanese director attached to the project, but
                  this is unverified.
                </p>
                <p>
                  Two scripts for the series were completed; the first script, titled &quot;The Man from the Past&quot;, was
                  fully animated and had a music and effects track created for it. The project was terminated before a dialogue
                  track could be recorded. This cut of the first episode has managed to surface here and there but has been
                  ravaged by time and countless format conversions and duplications. It was not until the release of the
                  <em>Master File</em> OVA on 2012 March 28 that a pristine copy was available. The episode comes with a Japanese
                  commentary track and Japanese subtitles for the dialogue.
                </p>
                <p>
                  Production cels from the series crop up occasionally on auctions sites, but due to their rarity, they quickly
                  soar in price. A manga version of the first episode was also released in Japan but is also a difficult item
                  to find.
                </p>
