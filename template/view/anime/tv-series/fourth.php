                <p>
                  The &quot;Blue Jacket&quot; series was announced at the <a href="http://en.wikipedia.org/wiki/MIPCOM" target="_blank" class="linked color-green">MIPCOM</a>
                  event in Cannes, France on 2014 October 14. The first series to star Lupin in thirty years is set in
                  Italy and San Marino. The series is in the hands of solid Lupin veterans with Kazuhide Tomonaga, (key
                  animator on <a href="/anime/theatrical-films/secret-of-mamo" class="linked color-green">Mamo</a> and <a href="/anime/theatrical-films/castle-of-cagliostro" class="linked color-green">Cagliostro</a>
                  as chief director, Yuichiro Yano directing at Telecom Animation Film, and  Yuuya Takahashi (<a href="/anime/theatrical-films/jigen-daisuke-gravestone" class="">Jigen Daisuke no Bohy≈ç</a>)
                  in charge of the series scripts.
                </p>
                <p>
                  The series is set to open with Zenigata receiving an invitation to Lupin's wedding. Lupin is marrying
                  Rebecca Rossellini, the young president of a conglomerate based in the Republic of San Marino. She has
                  also worked as a model and an actress, and has been rumored to be involved in a number of celebrity
                  affairs. She has an independent wild streak and has a secret she cannot tell anyone.
                </p>
                <p>
                  It was expected to initially be broadcast in Italy in 2015 May with a Japanese broadcast in fall 2015.
                  The series was delayed in Italy until August with the Japanese broadcast beginning a little over a month
                  later. Two episodes were broadcast in Italy but reserved as DVD / Blu-ray only episodes in Japan.
                </p>
