            <div class="row">
              <div class="col-md-10">
                <p>
                  The famous &quot;green jacket&quot; series was created with an adult sensibility and attempted to create a
                  stylish series not previously seen. Great attention was given to the details of the automobiles, guns, and
                  brand name items used. Initially, the series focused less on humor and more on pulp stories centered on a
                  particular heist. Lupin would not hesitate to kill anyone in his way though he did have his own skewed moral
                  code.
                </p>
                <p>
                  The series opened to poor ratings, and director Osumi Masaki's refusal to alter the tone of the series lead
                  to his removal by the time the third episode had aired. In an attempt to reach a broader audience, Miyazaki
                  Hayao and Takahata Isao were brought in to soften the series and provide more humor. Despite their efforts,
                  the series was canceled after twenty-three episodes. However, the series found its audience in reruns and
                  grew in popularity.
                </p>
