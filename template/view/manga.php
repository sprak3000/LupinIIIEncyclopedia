          <div id="lupin-iii-y" class="tab-pane fade in">
            <p>
              Monkey Punch finally took hold of the reins again, and started writing new stories for <em>Weekly Manga Action</em>
              on 1999 May 27. The &quot;Y&quot; for this series stands for the illustrator Monkey Punch tapped to do the
              art, Yamakami Masatsuki. The series was published until 2004 August 28 resulting in another eighty chapters
              of adventure for the gang. When the series resumed in the summer of 2009, it found a new publication home
              in the pages of <em>Lupin III Official Magazine</em>. Reaching back to its roots, the series retitled itself
              <em>Shin Lupin III Y</em>.
            </p>

