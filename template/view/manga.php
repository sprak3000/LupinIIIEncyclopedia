          <div id="misc" class="tab-pane fade in">
            <p>
              The franchise has also had many other supplementary works created, typically in the form of Mooks and Roman
              Albums. Mooks are &quot;movie books&quot; encapsulating a summary of a film in storyboard fashion along
              with interviews, production notes, and other materials about the film. Roman Albums derive their name from
              the Japanese using the word &quot;roman&quot; for &quot;romance&quot;. Despite the name difference, these
              rarely contain any content different than what is found in a Mook.
            </p>
            <p>
              Various manga adaptations have been made of the TV series, specials, and films, <em>Cagliostro</em> being
              a particular favorite with numerous releases. The most recent anime adapatation was for the <em>Lupin vs Conan</em>
              film.
            </p>

