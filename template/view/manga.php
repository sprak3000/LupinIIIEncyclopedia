          <div id="lupin-iii" class="tab-pane fade in active">
            <img class="img-responsive pull-right" src="/dist/asset/img/manga/weekly-manga-action.jpg" alt="Lupin III on the cover of Weekly Manga Action" />
            <p>
              Kazuhiko Kato started his publishing career in 1965 with <em>Playboy School</em> (プレーボーイ入門) under
              the pen name Gamuta Eiji (がむた永二) and was currently writing <em>Pinky Punky</em> (ピンキィ パンキィ), when
              he was approached to do a three month project for a new magazine called
              <em><a href="http://en.wikipedia.org/wiki/Weekly_Manga_Action" title="Wikipedia entry for Weekly Manga Action">Weekly Manga Action</a></em>.
              The editor suggested Kazuhiko use the pen name &quot;Monkey Punch&quot;; Kazuhiko reluctantly agreed to
              use it for a year. The three month project and one year pseudonym started on 1967 August 10 and
              turned into a franchise that has spanned decades and a name that is still recognized in the anime and manga community.
            </p>
            <p>
              The inspiration for the series came from Maurice Leblanc's series of <em>Ars&egrave;ne Lupin: Gentleman Thief</em>
              novels and short stories. No treasure was safe once Ars&egrave;ne Lupin sent warning to his upcoming victim.
              Monkey Punch made Lupin's grandson, Ars&egrave;ne Lupin III, the protagonist of his series and eventually
              created the supporting characters that would be the core of the series to this day. Monkey Punch took his
              inspiration for the art style from <em>Mad Magazine</em>, particularly Mort Drucker and Sergio Aragon&eacute;s.
            </p>
            <p>
              Where Leblanc's Lupin was a gallant gentleman thief, Monkey Punch's Lupin III was a lascivious, self absorbed
              lout. He spends most of his time trying to bed every woman he comes across, willing or not. However, his
              efforts usually resulted in cartoon-ish physical repercussions.
            </p>
            <p>
              The initial run of the manga ended on 1969 May 22 with ninety-four chapters penned. It would soon return
              two years later to <em>Weekly Manga Action</em> and end again on 1972 April 27 adding another thirty-six
              chapters to the series. But this was actually just the beginning...
            </p>
          </div>

          <div id="shin-lupin-iii" class="tab-pane fade in">
            <img class="img-responsive pull-right" src="/dist/asset/img/manga/weekly-manga-action.jpg" alt="Lupin III on the cover of Weekly Manga Action" />
            <p>
              Five years after Monkey Punch bid the gang farewell, he again returned to <em>Weekly Manga Action</em>
              with a series titled <em>Shin Lupin III</em> (<em>New Lupin III</em>). This series ran from 1977 June 23
              to 1981 May 28 adding another 188 chapters to Lupin's legacy. Many of the stories in this run would be
              adapted into episodes for the <a href="/anime/tv-series/second" class="linked color-green">second TV series</a>.
            </p>
          </div>

          <div id="lupin-iii-s" class="tab-pane fade in">
            <p>
              It would take sixteen years before Lupin would grace the world of manga. Starting on 1997 January 01, Monkey
              Punch supervised five stories by writer Takaguchi Satosumi and illustrator Shusay. Rather than retain
              Monkey Punch's distinctive art style, this manga resembled the animated depictions of the gang.
            </p>

