            <p>
              Monkey Punch teamed up with illustrator Miyama Yukio (the &quot;M&quot; in the title if you prefer) to
              produce a fifth manga series. The series began in 2004 in the pages of <em>Lupin III Official Magazine</em>.
              It would churn out another thirty-six chapters before going on hiatus until 2009. When it returned, it was
              surprisingly not under a name that started with &quot;Shin&quot; &ndash; <em>Lupin III M Neo</em>.
            </p>
          </div>

          <div id="lupin-iii-h" class="tab-pane fade in">
            <img class="img-responsive pull-right" src="/dist/asset/img/manga/lupin-iii-h-manga.jpg" alt="Lupin III H manga cover" />
            <p>
              No, it's not the sort of &quot;H&quot; typically associated with manga... Monkey Punch partnered with
              illustrator Hayakawa Naoya in the summer 2009 issue of <em>Lupin III Official Magazine</em> for the sixth
              manga entry. Currently, there are three volumes published that collect the current run of this incarnation.
            </p>
          </div>

          <div id="tokyopop" class="tab-pane fade in">
            <p>
              While Italy and other nations had translations of the original two manga series, the US would not receive
              its own translation until TokyoPop began publishing the first series on 2002 December 02. They would release
              the entire series in fourteen volumes with the last being released on 2004 July 06. Each volume retailed for
              $9.99 and was sized 5 x 7.5 inches.
            </p>
            <p>
              TokyoPop began their release of the second series, titled <em>Lupin III - World's Most Wanted</em>, on 2004
              September 07. Sadly, the run was cancelled due to poor sales and TokyoPop's own decline as a company.
              Volume nine was the last one published on 2007 July 10. It is unlikely any one will pick up and finish
              the run here in the US. Each volume retailed for $9.99 and was sized 5 x 7.5 inches.
            </p>

